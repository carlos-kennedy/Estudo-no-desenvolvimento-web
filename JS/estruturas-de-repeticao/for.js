// for

// break - Para a execução do loop
// continue - Pula a execução do momento

// O i é a variável

// incrementado
for (let i = 0; i <= 20; i++) {
  // console.log("o i é: ", i);
}

// decrementado
for (let i = 200; i > 0; i--) {
  // console.log("o i é: ", i);
}

// com condição e interrompendo a repetição
for (let i = 139; i >= 0; i--) {
  if (i === 39) {
    break;
  }
  // console.log(i);
}

//Continuando o loop.
for (let i = 10; i >= 0; i--) {
  if (i === 10) {
    continue;
  }
  console.log(i);
}
