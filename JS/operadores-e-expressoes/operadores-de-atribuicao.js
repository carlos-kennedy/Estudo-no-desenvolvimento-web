// Operadores de atribuição (Assignment)

let x;
let y;

// console.log(x, y)

// Assignment
x = 2;
y = 2;

// Addition assignment
// Faz com que adicione mais pelo valor dado
// Sem o operador de atribuição
// x = x + 2
// y = y + 2
// x += 2;
// y += 3;
x += y;

console.log(x, y);

