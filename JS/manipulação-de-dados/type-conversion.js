// Type conversion (type casting) vs Type coersion
// Alteração de um tipo de dado para outro tipo

// Exemplo de coersion (forçar troca)
console.log('9' + 5) //O programa fez uma troca forçada para que não mostre nenhum erro e concatenando o number com uma string

// Exemplo de Type conversion
console.log(Number('9') + 5) //Faz com altere o tipo de dado e convertendo para outro tipo (String para number)